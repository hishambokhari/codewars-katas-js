// Declaring a variable


// using let - allows for reassigning 
let myDogsName = "Zeus";
myDogsName = 'Lui' // reassigning dogs name from zeus to lui
console.log(myDogsName);

// using const - locks the variable - cannot reassign

const myDogsNewName = "Roger";
// myDogsNewName = "Timmy" --  this will give an error
console.log(myDogsNewName);

